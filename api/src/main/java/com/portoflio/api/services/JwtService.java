    public String generateToken(
            Authentication authentication
    ) {
        return buildToken(authentication, jwtExpiration);
    }

    public String generateRefreshToken(
            Authentication authentication
    ) {
        return buildToken(authentication, refreshExpiration);
    }

    public String buildToken(Authentication authentication, long expiration) {
        Instant now = Instant.now();
        String scope = authentication.getAuthorities().stream()
                .map(GrantedAuthority::getAuthority)
                .collect(Collectors.joining(" "));
        JwtClaimsSet claims = JwtClaimsSet.builder()
                .issuer("self")
                .issuedAt(now)
                //.expiresAt(now.plus(1, ChronoUnit.HOURS))
                .expiresAt(now.plus(expiration, ChronoUnit.HOURS))
                .subject(authentication.getName())
                .claim("scope", scope)
                .build();
        return this.encoder.encode(JwtEncoderParameters.from(claims)).getTokenValue();
    }

    public String extractUsername(String token) {
        return extractClaim(token, Claims::getSubject);
    }

    public <T> T extractClaim(String token, Function<Claims, T> claimsResolver) {
        final Claims claims = extractAllClaims(token);
        return claimsResolver.apply(claims);
    }

